    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String sno = jTable1.getValueAt(jTable1.getSelectedRow(),0).toString();
        updateid = sno;
        Connection con;
        con = Connections.getConnection();
        try{
            //based on item user selects, the inital stock value should display on txtbox
            String sql = "Select * from Items where ItemID=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, sno);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                txtinitialstock.setText(rs.getString("InitialStock"));
            }
        }catch(Exception Ex){
            System.out.println("An error occurred" + Ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //update button
        //gets user selected of data entry 
        if(txtinitialstock.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please Selecr from JTable");
        }else {
            Connection con;
            try{
                con = Connections.getConnection();//connects to database 
                
                //takes number typed by user in txtbox and updates current value to that number 
                String sql = "update Items set InitialStock=? where ItemID="+updateid;
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, txtinitialstock.getText());
                int ok = JOptionPane.showConfirmDialog(rootPane, "Sure you wish to update stock?", "Update Stock?", JOptionPane.YES_NO_OPTION);
                if(ok==0){
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Data has been updated", "Done", 1);
                    txtinitialstock.setText("");
                    ClearFields();
                }
            }catch(Exception Ex){
                System.out.println("An error occurred" + Ex);
            }
        }
    }
            
        
