    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
         try{
            con = Connections.getConnection();//Connection to database
            pst = con.prepareStatement("select * from Items");
            rs = pst.executeQuery();
            
            while(rs.next()){
                //this adds data until program is finished
               String ItemID = String.valueOf(rs.getInt(1));
               String Item = rs.getString(2);
               String Description = rs.getString(3);
               String InitialStock = String.valueOf(rs.getInt(4));
               String Category = rs.getString(5);


                //String array is used to store the values into the table
                String Customers[] = {ItemID, Item, Description, InitialStock, Category};
                    DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                    
                    //adding string array into the actual table to display data 
                    tblModel.addRow(Customers);
                    
                    
                    con.close();
            }
        }catch(Exception Ex){
            System.out.println("An error occurred" + Ex);
        }                                    
    }//GEN-LAST:event_jButton1ActionPerformed

